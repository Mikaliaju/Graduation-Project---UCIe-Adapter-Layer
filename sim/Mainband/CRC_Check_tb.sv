`timescale 1ns/1ps

module CRC_Check_tb;

  // ??? Parameters ????????????????????????????????????????????????????????????
  localparam DATA_PATH = 512;
  localparam CRC_SIZE  = 16;
  localparam CLK_PERIOD = 10;

  // ??? DUT Signals ???????????????????????????????????????????????????????????
  logic                    clk;
  logic                    rst_n;
  logic                    crc_payload_valid;
  logic [DATA_PATH-1:0]    crc_payload;
  logic [CRC_SIZE-1:0]     crc0_ch;
  logic [CRC_SIZE-1:0]     crc1_ch;
  logic                    crc_correct;
  logic                    crc_err;

  // ??? DUT Instantiation ?????????????????????????????????????????????????????
  CRC_Check dut (
    .i_clk               (clk),
    .i_rst_n             (rst_n),
    .i_crc_payload_valid (crc_payload_valid),
    .i_crc_payload       (crc_payload),
    .i_crc0_ch           (crc0_ch),
    .i_crc1_ch           (crc1_ch),
    .o_crc_correct       (crc_correct),
    .o_crc_err           (crc_err)
  );

  // ??? Clock ?????????????????????????????????????????????????????????????????
  initial clk = 0;
  always #(CLK_PERIOD/2) clk = ~clk;

  // ??? Task: Reset ???????????????????????????????????????????????????????????
  task apply_reset();
    rst_n             = 0;
    crc_payload_valid = 0;
    crc_payload       = 0;
    crc0_ch           = 0;
    crc1_ch           = 0;
    repeat(4) @(posedge clk);
    #1;
    rst_n = 1;
    @(posedge clk);
    $display("[%0t] Reset released", $time);
  endtask

  // ??? Task: Send Flit ???????????????????????????????????????????????????????
  // valid stays HIGH for 5 cycles:
  //   Cycle 1?4 : data chunks
  //   Cycle 5   : S_COMPARE fires (valid still = 1)
  task send_flit(
    input logic [DATA_PATH-1:0] chunk0, chunk1, chunk2, chunk3,
    input logic [CRC_SIZE-1:0]  exp_crc0, exp_crc1,   // expected CRC values (from CRC_Generator)
    input logic                 inject_err,            // 1 = send wrong CRC to test error detection
    input string                test_name
  );
    $display("\n[%0t] ?? START: %s ??", $time, test_name);

    // Cycle 1 ? Chunk 0
    @(posedge clk); #1;
    crc_payload_valid = 1;
    crc_payload       = chunk0;
    crc0_ch           = inject_err ? ~exp_crc0 : exp_crc0;
    crc1_ch           = inject_err ? ~exp_crc1 : exp_crc1;

    // Cycle 2 ? Chunk 1
    @(posedge clk); #1;
    crc_payload = chunk1;

    // Cycle 3 ? Chunk 2
    @(posedge clk); #1;
    crc_payload = chunk2;

    // Cycle 4 ? Chunk 3
    @(posedge clk); #1;
    crc_payload = chunk3;

    // Cycle 5 ? S_COMPARE (valid still HIGH)
    @(posedge clk); #1;
    crc_payload = 0;   // don't care, state is S_COMPARE

    // De-assert valid
    @(posedge clk); #1;
    crc_payload_valid = 0;

    // Sample result
    @(posedge clk);
    if (crc_correct && !crc_err)
      $display("[%0t]  ? PASS ? crc_correct=1, crc_err=0", $time);
    else
      $display("[%0t]  ? FAIL ? crc0_ch=%0h, crc1_ch=%0h", $time, crc0_ch, crc1_ch);

    $display("[%0t] ?? END: %s ??", $time, test_name);
  endtask

  // ??? Main Test Sequence ????????????????????????????????????????????????????
  initial begin
    $dumpfile("crc_check_tb.vcd");
    $dumpvars(0, CRC_Check_tb);

    apply_reset();

    // ?? Test 1: All-Zeros ? correct CRC ??????????????????????????????????
    // CRC values here are placeholders ? replace with real values from CRC_Generator
    send_flit(
      {DATA_PATH{1'b0}}, {DATA_PATH{1'b0}},
      {DATA_PATH{1'b0}}, {DATA_PATH{1'b0}},
      16'hAAAA, 16'hBBBB,   // ? replace with real CRC from generator
      0,                     // no error injection
      "All-Zeros ? Correct CRC"
    );

    repeat(2) @(posedge clk);

    // ?? Test 2: All-Zeros ? wrong CRC (error detection) ??????????????????
    send_flit(
      {DATA_PATH{1'b0}}, {DATA_PATH{1'b0}},
      {DATA_PATH{1'b0}}, {DATA_PATH{1'b0}},
      16'hAAAA, 16'hBBBB,   // ? replace with real CRC from generator
      1,                     // inject error ? send ~CRC
      "All-Zeros ? Wrong CRC (Error Inject)"
    );

    repeat(2) @(posedge clk);

    // ?? Test 3: All-Ones ? correct CRC ???????????????????????????????????
    send_flit(
      {DATA_PATH{1'b1}}, {DATA_PATH{1'b1}},
      {DATA_PATH{1'b1}}, {DATA_PATH{1'b1}},
      16'hCCCC, 16'hDDDD,   // ? replace with real CRC from generator
      0,
      "All-Ones ? Correct CRC"
    );

    repeat(2) @(posedge clk);

    // ?? Test 4: Two Consecutive Flits ?????????????????????????????????????
    $display("\n[%0t] ?? TEST: Consecutive Flits ??", $time);

    send_flit(
      512'hAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAA,
      512'h5555555555555555555555555555555555555555555555555555555555555555555555555555555555555555555555555555555555555555555555555555555555555555,
      512'hAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAA,
      512'h5555555555555555555555555555555555555555555555555555555555555555555555555555555555555555555555555555555555555555555555555555555555555555,
      16'hEEEE, 16'hFFFF,   // ? replace with real CRC from generator
      0,
      "Flit #1 ? Alternating Pattern"
    );

    send_flit(
      {DATA_PATH{1'b0}}, {DATA_PATH{1'b0}},
      {DATA_PATH{1'b0}}, {DATA_PATH{1'b0}},
      16'hAAAA, 16'hBBBB,   // ? replace with real CRC from generator
      0,
      "Flit #2 ? All-Zeros"
    );

    repeat(4) @(posedge clk);

    $display("\n[??????????????????????????????????");
    $display("All tests done!");
    $display(" ??????????????????????????????????\n", $time);
    $stop;
  end

endmodule

